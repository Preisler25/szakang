# you can comment with a hash
# instade of curly braces, python uses colons for function and loop blocks
def main():
    # you don't need to declare variable types
    b = 2
    # you don't need to use curly braces for loops
    for a in range(5):
        # you don't need to use parentheses for if statements
        if a < b:
            # you don't need to use curly braces for code blocks
            # you don't need to use semicolons to end lines
            print("a is less than b")
        else:
            print("a is greater than or equal to b")
main()